    class RetrieveFeedTask extends AsyncTask<Void, Void, String> {

        private Exception exception;

        protected void onPreExecute() {
            progressBar.setVisibility(View.VISIBLE);
            responseView.setText("");
        }

        protected String doInBackground(Void... urls) {
            try {
                URL url = new URL(API_URL);
                HttpURLConnection urlConnection = (HttpURLConnection) url.openConnection();
                try {
                    BufferedReader bufferedReader = new BufferedReader(new InputStreamReader(urlConnection.getInputStream()));
                    StringBuilder stringBuilder = new StringBuilder();
                    String line;
                    while ((line = bufferedReader.readLine()) != null) {
                        stringBuilder.append(line).append("\n");
                    }
                    bufferedReader.close();
                    return stringBuilder.toString();
                }
                finally{
                    urlConnection.disconnect();
                }
            }
            catch(Exception e) {
                Log.e("ERROR", e.getMessage(), e);
                return null;
            }
        }

        protected void onPostExecute(String response) {
            if(response == null) {
                response = "THERE WAS AN ERROR";
            }
            progressBar.setVisibility(View.GONE);
            String data = "";
            try{
                //JSONObject  jsonRootObject = new JSONObject(response);
                JSONArray jsonArray = new JSONArray(response);
                for(int i=0; i < jsonArray.length(); i++){
                    JSONObject jsonObject = jsonArray.getJSONObject(i);
                    int id = Integer.parseInt(jsonObject.optString("idArticle").toString());
                    String time = jsonObject.optString("ArticleDateTime").toString();
                    String title = jsonObject.optString("ArticleTitle").toString();
                    String desc = jsonObject.optString("ArticleText").toString();
                    data += "Node"+i+" : \n idArticle= "+ id +" \n Article date= "+ time +" \n Title= "+ title +" \n" + desc + "\n ";
                }
                responseView.setText(data);
            } catch (JSONException e) {e.printStackTrace();}
        }
    }

